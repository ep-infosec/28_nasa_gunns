const int TS_random::RAND_CNT = 1000;

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
int TS_random::boundSeed(
   int seed )
{
    return ( abs( seed ) % RAND_CNT );
}


///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
int TS_random::incrementSeed(
    int seed )
{
   // make sure seed is in allowed range, in case someone has set the seed directly
   return ( ( boundSeed( seed ) + 1 ) % RAND_CNT );
}


///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
double TS_random::randomDouble( 
    int & seed )
{
    seed = incrementSeed( seed );
    return RAND_VALUES[ seed ];
}


///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
double TS_random::randomDouble(
    int  & seed,
    double maxValue )
{
    return randomDouble( seed, 0.0, maxValue );
}


///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
double TS_random::randomDouble(
    int    & seed,
    double   minValue,
    double   maxValue )
{
    seed = incrementSeed( seed );

    if ( maxValue < minValue )
    {
        double temp = maxValue;
        maxValue = minValue;
        minValue = temp;
    }

    double range = maxValue - minValue;

    return range * RAND_VALUES[seed] + minValue;
}


int TS_random::randomInt(
    int & seed )
{
    return randomInt( seed, 0, RAND_MAX - 1 );
}


int TS_random::randomInt(
    int & seed,
    int   maxValue )
{
    if ( 0 <= maxValue )
    {
        return randomInt( seed, 0, maxValue );
    }
    else
    {
        return randomInt( seed, maxValue, 0 );
    }
}


int TS_random::randomInt(
    int & seed,
    int   minValue,
    int   maxValue )
{
    seed = incrementSeed( seed );

    if ( minValue > maxValue )
    {
        int temp = minValue;
        minValue = maxValue;
        maxValue = temp;
    }

    int range = maxValue - minValue + 1;

    return floor( RAND_VALUES[seed] * range ) + minValue;
}




double TS_random::uniqueRandomDouble(
    int               & seed,
    double              minValue,
    double              maxValue,
    std::vector<double> values )
{
    double value = randomDouble( seed, minValue, maxValue );

    while ( valueFound( value, values ) )
    {
        value = randomDouble( seed, minValue, maxValue );
    }

    return value;
}


bool TS_random::valueFound(
    double              value,
    std::vector<double> values )
{
    for (unsigned int index = 0; index < values.size(); index++ )
    {
        if ( ( value <= values[index] + DBL_EPSILON ) && ( value >= values[index] - DBL_EPSILON ) )
        {
            return true;
        }
    }

    return false;
}


